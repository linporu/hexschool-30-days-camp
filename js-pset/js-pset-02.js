const square = (num) => {
  return parseInt(num.toExponential(2));
};

const calculate = (num1, num2, num3) => {
  return num1 + num2 - num3;
};

const rounding = (num) => {
  return num.toFixed(2);
};

const parseNumber = (string) => {
  return Number(string);
};

const divisible = (num) => {
  return num % 2;
};
